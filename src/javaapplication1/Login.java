    private int ConfirmUserLogin(String user, String pass) {
        int count = 0;
        try {
            Connection con = new DBClass().getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select Username,Password from LoginDetails where Username = '" + user + "'and Password = '" + pass + "'");
            while (rs.next()) {
                count++;
            }

        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }

        return count;
    }

